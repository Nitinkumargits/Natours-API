import axios from 'axios';
import { showAlert } from './alerts';

export const login = async (email, password) => {
  try {
    const res = await axios({
      method: 'POST',
      url: 'http://127.0.0.1:3000/api/v1/users/login',
      data: {
        email,
        password
      }
    });

    if (res.data.status === 'success') {
      showAlert('success', 'Logged in successfully!');
      window.setTimeout(() => {
        location.assign('/');
      }, 1500);
    }
  } catch (err) {
    showAlert('error', err.response.data.message);
  }
};

export const logout = async () => {
  try {
    const res = await axios({
      method: 'GET',
      url: 'http://127.0.0.1:3000/api/v1/users/logout'
    });
    /**Reload the page (when we delete the cookie manually from the browser) since its is an ajax request we cannot do it on backend side ,can't doit with express, 
    - doing it manually here ,otherwise we would technically logout but our user menu would still reflect , it we will still show that we are logged inn , we need to reload the page which would then send the invalid cookie basically  to the server so that one that we recevied withour the token and then we are no longer loged in and therefore our user menu disappear 
     */
    if ((res.data.status = 'success')) location.reload(true);
  } catch (err) {
    console.log(err.response);
    showAlert('error', 'Error logging out! Try again.');
  }
};
